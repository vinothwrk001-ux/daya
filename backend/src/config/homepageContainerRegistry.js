function field(name, overrides = {}) {
  return {
    name,
    label: name
      .replace(/([A-Z])/g, " $1")
      .replace(/^./, (char) => char.toUpperCase()),
    ...FIELD_LIBRARY[name],
    ...overrides,
  };
}

