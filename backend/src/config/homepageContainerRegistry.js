function labelFromContainerType(type, prefix) {
  return String(type || "")
    .replace(new RegExp(`^${prefix}_`), `${prefix[0]}${prefix.slice(1).toLowerCase()} `)
    .replace(/_/g, " ")
    .replace(/\b\w/g, (char) => char.toUpperCase());
}

